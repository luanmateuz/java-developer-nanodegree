package lesson01.variablesanddatatypes.ch08;

public class Main {

    /**
     * Lesson 01 - Chapter 08
     * Third Bus Stop
     */
    public static void main(String[] args) {
        int passengers;
        passengers = 0;
        passengers = passengers + 5;
        passengers = passengers - 3;

        // update the variable passenger to subtract 1 and add 5
        passengers = passengers - 1 + 5;

        // displaying variables
        System.out.println(passengers);
    }
}
